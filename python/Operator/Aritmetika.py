

a = int(input("Masukan Nilai a disini = "))
b = int(input("Masukan Nilai b disini = "))

"""%d = float, %f = float (6 angka di belakang koma), %.2f = dua angka di belakang koma"""
# Operator Penjumlahan
c = a + b
print ("%d + %d = %d " % (a,b,c))

# Operator Pengurangan
c = a - b
print ("%d - %d = %d " % (a,b,c))

# Operator Perkalian
c = a * b
print ("%d * %d = %d " % (a,b,c))

# Operator Pembagian
c = a / b
print ("%d / %d = %.2f " % (a,b,c))

# Operator Sisa bagi
c = a % b
print ("%d %% %d = %.2f " % (a,b,c))

# Operator Pangkat
c = a ** b
print ("%d ** %d = %d " % (a,b,c))
